    public void tampil(){
        Object header[]={"Kode Supplier","Nama","Alamat","No. Telepon"};
        DefaultTableModel data=new DefaultTableModel(null,header);
        jTable1.setModel(data);
        setKoneksi();
        String sql="select*from tbsupplier";
        try {
            ResultSet rs=st.executeQuery(sql);
            while (rs.next())
            {
                String kolom1=rs.getString(1);
                String kolom2=rs.getString(2);
                String kolom3=rs.getString(3);
                String kolom4=rs.getString(4);
                String kolom[]={kolom1,kolom2,kolom3,kolom4};
                data.addRow(kolom);
            }
        } catch (Exception e) {
        }
    }
    
    private void simpan(){
        try{
            setKoneksi();
            String sql="insert into tbsupplier values('"+ kodeTF.getText() +"','" + namaTF.getText() 
                    + "','" + alamatTF.getText() + "','" + telpTF.getText() +"')";
            st.executeUpdate(sql); 
            JOptionPane.showMessageDialog(null,"Simpan data berhasil");
            }
            catch (Exception e) {
        }
        tampil();
    }
    
    private void perbarui(){
        try{
            setKoneksi();
            String sql="update tbsupplier set nmsupplier='"+namaTF.getText()+"',alamat='"+alamatTF.getText()+
                    "',telp='"+telpTF.getText()+"' where kdsupplier='"+kodeTF.getText()+"'";
            st.executeUpdate(sql);
            JOptionPane.showMessageDialog(null,"Edit data berhasil","Abadi Sport",JOptionPane.INFORMATION_MESSAGE);
        } 
        catch(Exception e){
        }
        tampil();
    }
    
    private void otomatis(){
        try {
            setKoneksi();
            String sql="select right (kdsupplier,2)+1 from tbsupplier";
            ResultSet rs=st.executeQuery(sql);
            namaTF.setText("");
            alamatTF.setText("");
            telpTF.setText("");
            if(rs.next()){
                rs.last();
                String no=rs.getString(1);
                while (no.length()<3){
                    no="0"+no;
                    kodeTF.setText("S"+no);}
                }
            else
            {
                kodeTF.setText("S001"); 
            }
            } catch (Exception e) 
            {
        }
    }
    
    private void hapus(){
        try{
            String sql="delete from tbsupplier where kdsupplier='"+ kodeTF.getText() +"'";
            st.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "Hapus data sukses");
            }
            catch (Exception e) {
            }
        tampil();
    }


